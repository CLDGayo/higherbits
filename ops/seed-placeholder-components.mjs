// ---------------------------------------------------------------------------
// Runtime user discovery
// ---------------------------------------------------------------------------
async function discoverUserId() {
  // Primary: username OR display_username = 'cozy_downloads'
  const { data: byName, error: byNameErr } = await supabase
    .from("users")
    .select("id, username, display_username")
    .or("username.eq.cozy_downloads,display_username.eq.cozy_downloads")
    .limit(1)

  if (byNameErr) {
    throw new Error(`Failed to query users by name: ${byNameErr.message}`)
  }
  if (byName && byName.length > 0) {
    console.log(
      `[discover] Found target user by name: id=${byName[0].id} ` +
        `username=${byName[0].username ?? "-"} display_username=${byName[0].display_username ?? "-"}`,
    )
    return byName[0].id
  }

  // Fallback: first user with a non-null username, ordered by created_at
  console.warn(
    "[discover] No user named 'cozy_downloads' found. Falling back to the earliest user with a username.",
  )
  const { data: fallback, error: fbErr } = await supabase
    .from("users")
    .select("id, username, created_at")
    .not("username", "is", null)
    .order("created_at", { ascending: true })
    .limit(1)

  if (fbErr) {
    throw new Error(`Failed to query fallback user: ${fbErr.message}`)
  }
  if (!fallback || fallback.length === 0) {
    throw new Error(
      "No users found in the database. Cannot attribute seeded components — aborting.",
    )
  }
  console.log(
    `[discover] Fallback user: id=${fallback[0].id} username=${fallback[0].username}`,
  )
  return fallback[0].id
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------
async function main() {
  console.log("[seed] Starting placeholder-component seed (INSERT-only, idempotent).")

  const userId = await discoverUserId()

  // Track everything we insert so we can roll back on failure.
  const insertedComponentIds = []
  const insertedDemoIds = []
  const insertedSubmissionIds = []

  const summary = { inserted: [], skipped: [] }

  try {
    for (const c of seedComponents) {
      // Idempotency: check by (user_id, component_slug)
      const { data: existing, error: existErr } = await supabase
        .from("components")
        .select("id")
        .eq("user_id", userId)
        .eq("component_slug", c.slug)
        .limit(1)

      if (existErr) {
        throw new Error(
          `Idempotency check failed for '${c.slug}': ${existErr.message}`,
        )
      }
      if (existing && existing.length > 0) {
        console.log(
          `[skip] '${c.slug}' already exists (component id=${existing[0].id}) — skipping.`,
        )
        summary.skipped.push({ slug: c.slug, id: existing[0].id })
        continue
      }

      // Insert component
      const { data: compRows, error: compErr } = await supabase
        .from("components")
        .insert({
          component_names: [c.displayName],
          name: c.machineName,
          component_slug: c.slug,
          description: c.description,
          code: c.code,
          demo_code: c.demoCode,
          user_id: userId,
          dependencies: {},
          demo_dependencies: {},
          direct_registry_dependencies: [],
          demo_direct_registry_dependencies: [],
          preview_url: PLACEHOLDER_PREVIEW, // NOT NULL — must be set
          is_public: true,
          license: "mit",
          registry: "ui",
        })
        .select("id")

      if (compErr || !compRows || compRows.length === 0) {
        throw new Error(
          `Insert failed for component '${c.slug}': ${compErr ? compErr.message : "no row returned"}`,
        )
      }
      const componentId = compRows[0].id
      insertedComponentIds.push(componentId)
      console.log(
        `[insert] component '${c.displayName}' (slug=${c.slug}) -> id=${componentId}`,
      )

      // Insert demo (required for the home page get_demos_list_v2 listing)
      const { data: demoRows, error: demoErr } = await supabase
        .from("demos")
        .insert({
          component_id: componentId,
          demo_code: c.demoCode,
          user_id: userId,
          demo_slug: "default",
          name: "Default",
          preview_url: PLACEHOLDER_PREVIEW,
          demo_dependencies: {},
          demo_direct_registry_dependencies: [],
        })
        .select("id")

      if (demoErr || !demoRows || demoRows.length === 0) {
        throw new Error(
          `Insert failed for demo of '${c.slug}': ${demoErr ? demoErr.message : "no row returned"}`,
        )
      }
      const demoId = demoRows[0].id
      insertedDemoIds.push(demoId)
      console.log(`[insert]   demo 'default' -> id=${demoId}`)

      // Best-effort: submission row (status featured) so it surfaces on the home feed.
      // Failure here is a WARN, not fatal — component + demo already exist.
      const { data: subRows, error: subErr } = await supabase
        .from("submissions")
        .insert({ component_id: componentId, status: "featured" })
        .select("id")

      if (subErr) {
        console.warn(
          `[warn]   submission insert skipped for '${c.slug}': ${subErr.message} ` +
            "(non-fatal; component still exists on profile page)",
        )
      } else if (subRows && subRows.length > 0) {
        insertedSubmissionIds.push(subRows[0].id)
        console.log(
          `[insert]   submission 'featured' -> id=${subRows[0].id}`,
        )
      }

      summary.inserted.push({
        slug: c.slug,
        name: c.displayName,
        componentId,
        demoId,
      })
    }
  } catch (err) {
    console.error(`\n[FAIL] ${err.message}`)
    console.error("[rollback] Deleting rows inserted by this run...")
    await rollback(insertedSubmissionIds, insertedDemoIds, insertedComponentIds)
    process.exit(1)
  }

  // ---- Report -----------------------------------------------------------
  console.log("\n========== SEED SUMMARY ==========")
  console.log(`user_id: ${userId}`)
  console.log(`inserted: ${summary.inserted.length}`)
  for (const i of summary.inserted) {
    console.log(`  + ${i.name} (slug=${i.slug}) component=${i.componentId} demo=${i.demoId}`)
  }
  console.log(`skipped (already existed): ${summary.skipped.length}`)
  for (const s of summary.skipped) {
    console.log(`  = ${s.slug} (component=${s.id})`)
  }
  console.log("==================================")
  console.log("[seed] Done.")
}

async function rollback(submissionIds, demoIds, componentId) {
  // Delete in FK-safe order: submissions -> demos -> components.
  // (demos & submissions cascade from components anyway, but we delete explicitly
  //  in case a component insert succeeded while its demo failed.)
  try {
    if (submissionIds.length) {
      await supabase.from("submissions").delete().in("id", submissionIds)
    }
    if (demoIds.length) {
      await supabase.from("demos").delete().in("id", demoIds)
    }
    if (componentId.length) {
      await supabase.from("components").delete().in("id", componentId)
    }
    console.error(
      `[rollback] Removed ${componentId.length} component(s), ${demoIds.length} demo(s), ${submissionIds.length} submission(s).`,
    )
  } catch (rbErr) {
    console.error(
      `[rollback] WARNING: rollback itself failed: ${rbErr.message}. ` +
        "Manual cleanup may be required for the ids listed above.",
    )
  }
}

main().catch((err) => {
  console.error(`[FATAL] Unhandled error: ${err.message}`)
  process.exit(1)
})
