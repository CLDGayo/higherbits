#!/usr/bin/env node
/**
 * seed-placeholder-components.mjs
 *
 * Inserts 6-8 polished cozy-claymorphism placeholder UI components (each with one demo)
 * into the higherbits.dev Supabase database, attributed to the `cozy_downloads` user.
 *
 * Run:  NEXT_PUBLIC_SUPABASE_URL=... SUPABASE_SERVICE_ROLE_KEY=... node ops/seed-placeholder-components.mjs
 *
 * IDEMPOTENT: skips any component whose (user_id, component_slug) already exists.
 * INSERT-only. On failure it deletes ONLY the rows this run inserted (tracked by id).
 * Never reads .env files; never prints secrets.
 *
 * ---------------------------------------------------------------------------
 * SCHEMA RECON (static, from apps/web/prisma/schema.prisma @ 2026-07-13)
 * ---------------------------------------------------------------------------
 * TABLE `components`  (@@unique([user_id, component_slug]))
 *   id                  Int     PK autoincrement          (DB-assigned)
 *   component_names     Json    NOT NULL, no default      -> array of display names e.g. ["Cushion Button"]
 *   description         String? nullable
 *   code                String  default "N/A"             -> the component source (.tsx)
 *   demo_code           String? nullable                  (per-component; real demo lives in `demos`)
 *   user_id             String  NOT NULL  FK users.id (NoAction)
 *   dependencies        Json?   nullable                  -> {} (react/tailwind only, no extra npm)
 *   is_public           Boolean default true
 *   downloads_count     Int     default 0
 *   likes_count         Int     default 0
 *   component_slug      String  NOT NULL   (unique w/ user_id)
 *   name                String  NOT NULL   (slug-style machine name)
 *   demo_dependencies   Json?   nullable                  -> {}
 *   preview_url         String  NOT NULL, NO DEFAULT      *** must be set; NULL breaks insert & UI expects a string ***
 *   license             String  default "mit"
 *   fts                 tsvector  (auto/generated by DB — do NOT set)
 *   direct_registry_dependencies      Json default "[]"
 *   registry            String  default "ui"
 *   demo_direct_registry_dependencies Json default "[]"
 *   ...many nullable extras (video_url, website_url, payment_url, pro_preview_image_url, etc.)
 *
 *   is_paid / price:  NOT MODELED as boolean/int columns. "Paid" components use `payment_url`
 *   (a checkout link) instead. There is NO clean integer price column on `components`
 *   (components_purchases.price_paid records purchases, not a listing price). DECISION:
 *   seed all 8 components FREE/public (payment_url = null). A single "Pro" listing would only
 *   be half-modeled (a URL with no price), so per the task's "only if modeled cleanly" clause
 *   we skip it. See report.
 *
 * TABLE `demos`  (@@unique([component_id, demo_slug]))
 *   id                  Int    PK autoincrement           (DB-assigned)
 *   component_id        Int    NOT NULL  FK components.id (Cascade)
 *   demo_code           String NOT NULL                   -> the demo/usage source (.tsx)
 *   user_id             String NOT NULL  FK users.id (NoAction)
 *   demo_slug           String default "default"
 *   preview_url         String? nullable                  -> set to same placeholder as component
 *   name                String? nullable                  -> "Default"
 *   demo_dependencies   Json?  default "{}"
 *   demo_direct_registry_dependencies Json? default "[]"
 *   fts / embedding / embedding_oai  (auto/generated — do NOT set)
 *
 * TABLE `submissions` (@@unique component_id)  status submission_status default on_review
 *   The home page lists DEMOS via RPC get_demos_list_v2(p_include_private:false). That RPC is a
 *   server-side DB function (body not statically visible). transformDemoResult defaults
 *   submission_status -> "featured", and the marketplace convention is that a component is
 *   surfaced once its submission is `featured`. To maximize the chance the seeded rows render on
 *   the home + /cozy_downloads pages, this script best-effort inserts a submissions row with
 *   status='featured' per component. If the submissions insert is rejected (e.g. RLS/policy),
 *   it is logged as a WARN and does NOT fail the run — the component + demo still exist and the
 *   username profile page (get_user_profile_demo_list_v2) does not require a featured submission.
 *
 * TABLE `tags` / `component_tags` / `demo_tags`
 *   tags(id PK, name UNIQUE, slug UNIQUE). component_tags(component_id, tag_id) composite PK.
 *   demo_tags(id PK, demo_id, tag_id, @@unique(demo_id,tag_id)).
 *   Tags are OPTIONAL for rendering (home/profile RPCs don't require them; filtering-by-tag does).
 *   This script does NOT create tags — it keeps the blast radius minimal and avoids guessing the
 *   RPC's tag-join expectations. Components render tagless. (Noted in report.)
 *
 * RUNTIME DISCOVERY (no static DB reads by the author):
 *   user_id is discovered at runtime — SELECT users where username='cozy_downloads' OR
 *   display_username='cozy_downloads'; fallback = first user ordered by created_at with a
 *   non-null username.
 * ---------------------------------------------------------------------------
 */

